package _Ex_25;

import java.util.Scanner;

/*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::FIM IMPORTS::::::::*/


/*%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%*/

/*::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
public class ParOuImpar {
/*::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
    
    /**Verifica se o numero digitado é par ou impar*/
    public static void inicio(){
        Scanner input = new Scanner(System.in);
        
        System.out.println("Verifique se um inteiro é par ou impar");
        System.out.print("Digite um inteiro: ");
        
        int iNumero = input.nextInt();
        
        if (iNumero % 2 == 0){
            System.out.println("Numero digitado é par");
        }

        if (iNumero % 2 == 1){
            System.out.println("Numero digitado é impar");
        } 
    }   
}/*::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::FIM::::::*/

